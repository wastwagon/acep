/**
 * Generates a static route map for ACEP snapshots.
 *
 * Output: src/lib/acep-route-map.ts
 *
 * This is used by middleware to route any /<acep-path> to /acep/<acep-path>
 * while keeping our custom pages (/contracts, /electricity, etc.) intact.
 */

import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";

function normalizePathname(p) {
  if (!p.startsWith("/")) p = `/${p}`;
  // ensure trailing slash except root
  if (p !== "/" && !p.endsWith("/")) p = `${p}/`;
  return p;
}

async function main() {
  const contentIndexPath = path.resolve(process.cwd(), "content", "acep", "index.json");
  const raw = await readFile(contentIndexPath, "utf8");
  const idx = JSON.parse(raw);
  const pages = Array.isArray(idx.pages) ? idx.pages : [];

  const paths = new Set();
  for (const p of pages) {
    if (!p?.url || p.status !== 200) continue;
    try {
      const u = new URL(p.url);
      if (u.host !== "acep.africa" && u.host !== "www.acep.africa") continue;
      paths.add(normalizePathname(u.pathname));
    } catch {
      // ignore
    }
  }

  // Remove root "/" to avoid rewriting the platform homepage
  paths.delete("/");

  const sorted = Array.from(paths).sort((a, b) => a.localeCompare(b));

  const outDir = path.resolve(process.cwd(), "src", "lib");
  await mkdir(outDir, { recursive: true });
  const outFile = path.join(outDir, "acep-route-map.ts");

  const file = `// AUTO-GENERATED by scripts/generate-acep-route-map.mjs\n// Do not edit manually.\n\nexport const ACEP_PATHS = new Set<string>([\n${sorted
    .map((p) => `  ${JSON.stringify(p)},`)
    .join("\n")}\n]);\n`;

  await writeFile(outFile, file, "utf8");
  console.log(`[acep-route-map] Wrote ${sorted.length} paths to ${path.relative(process.cwd(), outFile)}`);
}

main().catch((e) => {
  console.error(e?.stack || e);
  process.exit(1);
});

